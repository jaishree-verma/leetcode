# \ -> escape charcter
# \" -> escape sequence
# \"
# \'
# \\
# \n
course = "Python \nProgramming"
print(course)
course = "Python \\ Programming"
print(course)
course = "Python \ Programming"
print(course)
course = "Python \' Programming"
print(course)
course = "Python \" Programming"
print(course)
